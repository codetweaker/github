        <h1>Questions and Answers </h1>
       
              <h4><span class="firstcharacter">Q</span>: Why should I choose SX3? </h4>
          <p><strong>SX3</strong> is the safe and simple hair transformation system. Cysteine
           protein and Glycolic Acid are the key ingredients in the Smoothing
           Treatment. Glycolic Acid penetrates throughout the hair shaft,
           enhancing softness, protection and manageablity. Cysteine proteins are
           carried with the Glycolic acid and deposited on and under the cuticle,
           softening the hair and weighing down the curl. Cysteine plays a
           valuable role by crosslinking proteins,which increases the rigidity of
           proteins. This provides your client with manageable hair that can be
           straightened easily with a brush and a blowdryer or left for a soft
           and shiny, frizz free curl. No chemicals are used to break the protein
           bonds in the hair so absolutely no harm comes to the hair from
           providing a service.</p>

          <h4><span class="firstcharacter">Q</span>: What is Glycolic Acid?</h4>
          <p>Glycolic Acid has been used safely for years in the cosmetic industry
           for skin rejuvenation but has recently been providing amazing benefits
           to the hair industry. It has been found that Glycolic acid makes hair
           more manageable, easier to style and significantly less prone to
           breakage.</p>

          <h4><span class="firstcharacter">Q</span>: How much SX3 Smoothing Treatment do I use?</h4>
          <p>Only 1-2 ounces of <strong>SX3</strong> per head is needed to achieve amazing, transforming results. Very little product is used so there is no fumes and product cost is minimal.</p>

          <h4><span class="firstcharacter">Q</span>: Should I be concerned about fumes?</h4>
          <p>There is no harsh chemicals in the <strong>SX3 Smoothing Treatment</strong> and very
           little product is required for optimum results. If there are
           noticeable fumes, you are probably using too much product but don’t be
           concerned, there are no harmful chemicals in the <strong>SX3 Smoothing
           Treatment</strong>.</p>

          <h4><span class="firstcharacter">Q</span>: Can SX3 be used on any hair type?</h4>
          <p>Protein treatments will not damage hair, but the heat applied when
           bonding a protein treatment to the hair can. Most smoothing systems
           use temperatures approaching 450 degrees F. Hair will burn at 451
           degrees so anything close to that temperature can be very damaging.
           With <strong>SX3 Smoothing Treatment</strong>, very low temperatures are required to
           close the cuticle. For fine, light hair, a 275°F-325°F (135-160°C)
           heat setting. For normal to coarse hair, we recommend a 350°F- 410°F
           (160- 205°C) heat setting. These low temperatures will never damage
           the hair.
        </p>

        <h4><span class="firstcharacter">Q</span>: Can SX3 be placed over relaxers or other smoothing treatments?</h4>
          <p>Yes, <strong>SX3 Smoothing Treatment</strong> can be applied over any relaxer. When
           applying <strong>SX3 Smoothing Treatment</strong> over any other Treatment make sure
           that it has been at least 2 months from the previous application.</p>

          <h4><span class="firstcharacter">Q</span>: Should SX3 be applied before or after a colour service?</h4>
          <p>We recommend that <strong>SX3 Smoothing Treatment</strong> be done after a colour
           service. Because colour opens the hair’s cuticle, we recommend
           replacing the use of <strong>SX3 Clarifying Shampoo</strong> with <strong>SX3 Keratin Balance
           Shampoo</strong>.</p>

          <h4><span class="firstcharacter">Q</span>: Can a client go swimming in a pool or the ocean after SX3 Treatment?</h4>
          <p>For the first two days, we recommend letting the hair settle. When
           clients go in the sun and water we recommend they use <strong>SX3 Leave In
           Treatment</strong> to protect the hair from the sun’s harmful rays and to
           re-apply a layer of keratin to the hair.
          </p>

          <h4><span class="firstcharacter">Q</span>: How long will an SX3 Treatment last in the hair? </h4>
          <p>Aftercare and maintenance is essential to the life of the treatment.
           Recommending <strong>SX3 Keratin Balance Shampoo</strong> and <strong>Conditioner</strong> along with
           the <strong>SX3 Leave In Treatment</strong> will extend the life of <strong>SX3 Smoothing
           Treatment</strong> and protect their investment. An <strong>SX3 Treatment</strong> lasts an
           average of 3-6 months.</p>

          
