<h1>Step-by-Step Instructions </h1>

<p>The shine provided is absolutely brilliant and the smoothing effect will last 3 months and longer if the <strong>SX3</strong> after care products are used. The product is rinsed out of the hair after processing and before blowdrying  which eliminates fumes and a second blowdrying step. The client is free to wash their hair anytime after the treatment. The results can be managed based on the time the<strong> SX3 Smoothing Treatment</strong> processes before being rinsed out. You can book another client while your <strong>Smoothing Treatment</strong> client is waiting.</p>

<h3>Step1: </h3>
<p>Wash the hair using the <strong>SX3 Clarifying Shampoo</strong> and leave on the hair for 5 minutes.<br></p><p>(This process will maximize the penetration of <strong>SX3 Smoothing System</strong>  into the hair cuticle.)</p>
<h3>Step 2:</h3>
<p>Towel dry hair well.</p>

<h3>Step 3: </h3>
<p>Section the hair.</p>

<h3>Step 4: </h3>
<p>Apply <strong>SX3 Smoothing System</strong>  in 1 inch sections using an application brush.</p>

<h3>Step 5: </h3>
<p>Once product has been applied on entire head, comb through to make sure product is evenly distributed.</p>



<h3>Step 6: </h3>
<p>Let product soak on hair 30-60 minutes (<a href="#result_chart">See Results Chart below</a>). Some stylists will bag the hair to intensify the results</p>

<h3>Step 7: </h3>
<p>Rinse hair thoroughly under running water for 5 minutes until water runs clear. It is very important to rinse well as the you are now neutralizing the PH of the hair.</p>

<h3>Step 8: </h3>
<p>Apply a deep conditioning treatment and let soak for 5 mins under the dryer.
Then rinse thoroughly. The hair is now be PH balanced and very healthy.</p>

<h3>Step 9: </h3>
<p>Use high heat and paddle brush, blow dry the hair 100&#37;.</p>

<h3>Step 10:</h3>
<p>Flat iron the hair in 1 inch sections between 275F- 410F degrees depending 
on porosity and hair strength.</p>


<p>Your client is free to wash their hair anytime. </p>

<h4> 
    Please be aware that a level of colour lift may occur as a result of a <strong>SX3 Smoothing System Treatment.</strong>  
